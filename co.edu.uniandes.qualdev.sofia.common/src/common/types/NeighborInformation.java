package common.types;

public class NeighborInformation {

	private int randomA;
	
	private int randomB;
	
	public NeighborInformation(){
		
	}

	public int getRandomA() {
		return randomA;
	}

	public void setRandomA(int randomA) {
		this.randomA = randomA;
	}

	public int getRandomB() {
		return randomB;
	}

	public void setRandomB(int randomB) {
		this.randomB = randomB;
	}
}
